/**
 * WildClawBench-CC runner — PilotDeck In-process Gateway edition.
 *
 * Parses WCB task .md files, prepares per-task workspaces, submits prompts
 * through PilotDeck's Gateway (in-process), collects workspace artifacts,
 * and runs the Python grade() function for scoring.
 *
 * Usage:
 *   node wcb/run_pilotdeck.mjs --task <task.md> [--output-dir <dir>] [--timeout <ms>]
 *   node wcb/run_pilotdeck.mjs --category <category_dir> [--limit N] [--filter <substr>]
 */

